	<script>
		function OfferingCardAndPromoHeight() {
			PromoHeight();
			CardHeight();
			if(window.innerWidth <= 719) {
				$(".promo-hero-content").css('margin-top', 0);
				VerticalAlignFirst(".promo-hero-content", ".promo-hero-text");
			} else {
				$(".promo-hero-text").css('margin-top', 0);
				VerticalAlignFirst(".promo-hero", ".promo-hero-content");
			}
		}
		function VerticalAlignFirst(ParentClass, ChildClass) {
            var ParentClassHeight = $(ParentClass).height();
            var ChildClassHeight = $(ChildClass).height();
            var Padding = ParentClassHeight - ChildClassHeight;
            Padding = Padding / 2;
            $(ChildClass).css('margin-top', Padding);
        }
		function CardHeight() {
			var HeaderHeight = $(".tall-heading").height();
			var ContentHeight = $(".tall-content").height();
			
			$(".offering-card-heading").height(HeaderHeight);
			$(".offering-card-real-text").height(ContentHeight);
		}
		function PromoHeight() {
			var viewPortWidth = window.innerWidth;
			if(viewPortWidth >= 720) {
				$('.promo-hero').height($(window).height());
				$('.promo-hero-content').height($(".promo-hero-text").height() + 32);
				$('.promo-hero').css('margin-top', '-' + $('.nav-bar').height() + 'px');
			}
			if(viewPortWidth <= 719) {
				$('.promo-hero-content').height($(window).height());
				$('.promo-hero').height($(window).height());
			}
		}
	</script>
	
 
